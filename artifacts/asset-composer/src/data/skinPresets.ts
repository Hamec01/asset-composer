export const SKIN_PRESETS: SkinPreset[] = [
  ...DARK_FANTASY_PRESETS,
  ...CUTE_FARM_PRESETS,
];

export function getPresetsByStyleSet(styleSetId: string): SkinPreset[] {
  return SKIN_PRESETS.filter(p => p.styleSetId === styleSetId);
}

export function getPresetById(id: string): SkinPreset | undefined {
  return SKIN_PRESETS.find(p => p.id === id);
}
